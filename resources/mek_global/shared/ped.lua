function getRandomSkin(gender)
	if gender then
		if gender == 0 then
			return availableSkins[0][1][math.random(#availableSkins[0][1])]
		elseif gender == 1 then
			return availableSkins[1][1][math.random(#availableSkins[1][1])]
		end
	end
	return false
end

function getRandomName(type, gender)
	if gender then
		if gender == 0 then
			gender = "male"
		elseif gender == 1 then
			gender = "female"
		end
	end

	if not type then
		type = "full"
	end

	if type == "full" then
		if not gender then
			gender = genders[math.random(#genders)]
		end
		local randFirstname
		if gender == "male" then
			randFirstname = males[math.random(#males)]
		elseif gender == "female" then
			randFirstname = females[math.random(#females)]
		end
		local randLastname = lastNames[math.random(#lastNames)]
		local name = tostring(randFirstname .. " " .. randLastname)
		return name
	elseif type == "last" then
		local randLastname = lastNames[math.random(#lastNames)]
		return randLastname
	elseif type == "first" then
		if not gender then
			gender = genders[math.random(#genders)]
		end
		local randFirstname
		if gender == "male" then
			randFirstname = males[math.random(#males)]
		elseif gender == "female" then
			randFirstname = females[math.random(#females)]
		end
		return randFirstname
	elseif type == "gender" then
		gender = genders[math.random(#genders)]
		if gender == "male" then
			return 0
		elseif gender == "female" then
			return 1
		end
	end
end

function getAvailableSkins()
	return availableSkins
end
